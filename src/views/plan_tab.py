import streamlit as st
import pandas as pd

# –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º –±–∏–∑–Ω–µ—Å-–ª–æ–≥–∏–∫—É –¥–ª—è —Å–∏–Ω—Ö—Ä–æ–Ω–Ω–æ–π –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏ –ø–ª–∞–Ω–∞
from src.services.content_gen import generate_content_plan

# ==========================================
# --- 1. –ì–õ–ê–í–ù–´–ô –ò–ù–¢–ï–†–§–ï–ô–° –í–ö–õ–ê–î–ö–ò ---
# ==========================================

def render_plan_tab() -> None:
    """–û—Ç—Ä–∏—Å–æ–≤—ã–≤–∞–µ—Ç –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å –≤–∫–ª–∞–¥–∫–∏ –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏ –∫–æ–Ω—Ç–µ–Ω—Ç-–ø–ª–∞–Ω–∞."""
    st.title("üìÖ –ö–æ–Ω—Ç–µ–Ω—Ç-–ø–ª–∞–Ω")
    st.markdown("–°–≥–µ–Ω–µ—Ä–∏—Ä—É–π—Ç–µ —Å—Ç—Ä–∞—Ç–µ–≥–∏—á–µ—Å–∫–∏–π –ø–ª–∞–Ω –ø—É–±–ª–∏–∫–∞—Ü–∏–π –¥–ª—è –≤–∞—à–∏—Ö —Å–æ—Ü–∏–∞–ª—å–Ω—ã—Ö —Å–µ—Ç–µ–π –∏ –±–ª–æ–≥–æ–≤.")

    # –°–æ–∑–¥–∞–µ–º –¥–≤—É—Ö–∫–æ–ª–æ–Ω–æ—á–Ω—ã–π –º–∞–∫–µ—Ç: —Å–ª–µ–≤–∞ –Ω–∞—Å—Ç—Ä–æ–π–∫–∏ (1 —á–∞—Å—Ç—å), —Å–ø—Ä–∞–≤–∞ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã (2.5 —á–∞—Å—Ç–∏)
    col_settings, col_results = st.columns([1, 2.5])

    # ==========================================
    # --- 2. –ò–ù–¢–ï–†–§–ï–ô–° –ù–ê–°–¢–†–û–ï–ö ---
    # ==========================================
    
    with col_settings:
        st.subheader("–ù–∞—Å—Ç—Ä–æ–π–∫–∏ –ø–ª–∞–Ω–∞")
        with st.form("plan_settings"):
            # –ß–∏—Ç–∞–µ–º –Ω–∏—à—É –∏–∑ —Å–µ—Å—Å–∏–∏ –∏–ª–∏ –∑–∞–¥–∞–µ–º –¥–µ—Ñ–æ–ª—Ç–Ω—É—é
            niche = st.text_input("–ù–∏—à–∞ –±–∏–∑–Ω–µ—Å–∞", value=st.session_state.get("current_niche", "–ö–æ–Ω—Å–∞–ª—Ç–∏–Ω–≥ –∏ —Ä–∞–∑—Ä–∞–±–æ—Ç–∫–∞ –ü–û"))
            
            period = st.selectbox("–ü–µ—Ä–∏–æ–¥", ["1 –Ω–µ–¥–µ–ª—è", "2 –Ω–µ–¥–µ–ª–∏", "1 –º–µ—Å—è—Ü"], index=0)
            channels = st.multiselect(
                "–ö–∞–Ω–∞–ª—ã —Ä–∞—Å–ø—Ä–æ—Å—Ç—Ä–∞–Ω–µ–Ω–∏—è", 
                ["Telegram", "–í–ö–æ–Ω—Ç–∞–∫—Ç–µ", "–ë–ª–æ–≥ (SEO)", "Email-—Ä–∞—Å—Å—ã–ª–∫–∞", "YouTube/Shorts"],
                default=["Telegram", "–í–ö–æ–Ω—Ç–∞–∫—Ç–µ"]
            )
            extra_context = st.text_area("–î–æ–ø. –∫–æ–Ω—Ç–µ–∫—Å—Ç (–ê–∫—Ü–∏–∏, —Ñ–æ–∫—É—Å –º–µ—Å—è—Ü–∞):", placeholder="–ù–∞–ø—Ä–∏–º–µ—Ä: –≤ —ç—Ç–æ–º –º–µ—Å—è—Ü–µ –ø—Ä–æ–¥–≤–∏–≥–∞–µ–º –Ω–æ–≤—ã–π –∫—É—Ä—Å...")
            
            generate_btn = st.form_submit_button("–°–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞—Ç—å –ø–ª–∞–Ω üöÄ", width='stretch')

    # ==========================================
    # --- 3. –ì–ï–ù–ï–†–ê–¶–ò–Ø –ò –í–´–í–û–î –†–ï–ó–£–õ–¨–¢–ê–¢–û–í ---
    # ==========================================
    
    with col_results:
        # –ï—Å–ª–∏ –∫–Ω–æ–ø–∫–∞ –Ω–∞–∂–∞—Ç–∞, –Ω–∞—á–∏–Ω–∞–µ–º –ø—Ä–æ—Ü–µ—Å—Å –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏
        if generate_btn:
            if not niche.strip():
                st.error("–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, —É–∫–∞–∂–∏—Ç–µ –Ω–∏—à—É –±–∏–∑–Ω–µ—Å–∞.")
            elif not channels:
                st.error("–í—ã–±–µ—Ä–∏—Ç–µ —Ö–æ—Ç—è –±—ã –æ–¥–∏–Ω –∫–∞–Ω–∞–ª —Ä–∞—Å–ø—Ä–æ—Å—Ç—Ä–∞–Ω–µ–Ω–∏—è.")
            else:
                with st.spinner("üß† –ù–µ–π—Ä–æ—Å–µ—Ç—å –∞–Ω–∞–ª–∏–∑–∏—Ä—É–µ—Ç –Ω–∏—à—É –∏ —Å–æ—Å—Ç–∞–≤–ª—è–µ—Ç –ø–ª–∞–Ω..."):
                    try:
                        # –°–æ—Ö—Ä–∞–Ω—è–µ–º –Ω–∏—à—É –≥–ª–æ–±–∞–ª—å–Ω–æ, —á—Ç–æ–±—ã –æ–Ω–∞ –ø–æ–¥—Ç—è–Ω—É–ª–∞—Å—å –≤ –¥—Ä—É–≥–∏—Ö –≤–∫–ª–∞–¥–∫–∞—Ö
                        st.session_state.current_niche = niche
                        
                        # –í—ã–∑—ã–≤–∞–µ–º –±–∏–∑–Ω–µ—Å-–ª–æ–≥–∏–∫—É (–≤–µ—Ä–Ω–µ—Ç —Å–ø–∏—Å–æ–∫ Pydantic-—Å—Ö–µ–º ContentPlanItem)
                        plan_items = generate_content_plan(
                            niche=niche, 
                            period=period, 
                            channels=", ".join(channels), 
                            extra_context=extra_context
                        )
                        
                        # –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º Pydantic-–º–æ–¥–µ–ª–∏ –≤ –æ–±—ã—á–Ω—ã–µ —Å–ª–æ–≤–∞—Ä–∏ –¥–ª—è Pandas
                        st.session_state.content_plan = [item.model_dump() for item in plan_items]
                        
                        # –ò–∑–≤–ª–µ–∫–∞–µ–º —É–Ω–∏–∫–∞–ª—å–Ω—ã–µ —Ç–µ–º—ã –¥–ª—è –∏–Ω—Ç–µ–≥—Ä–∞—Ü–∏–∏ —Å –≤–∫–ª–∞–¥–∫–æ–π "–†–∞—Å—Å—ã–ª–∫–∏"
                        st.session_state.generated_topics = list(set([item.topic for item in plan_items]))
                        
                        st.success("‚úÖ –ö–æ–Ω—Ç–µ–Ω—Ç-–ø–ª–∞–Ω —É—Å–ø–µ—à–Ω–æ —Å—Ñ–æ—Ä–º–∏—Ä–æ–≤–∞–Ω!")
                    except Exception as e:
                        st.error(f"‚ùå –û—à–∏–±–∫–∞ –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏: {str(e)}")

        # ==========================================
        # --- 4. –û–¢–†–ò–°–û–í–ö–ê –¢–ê–ë–õ–ò–¶–´ –ò –≠–ö–°–ü–û–†–¢ ---
        # ==========================================
        
        # –ï—Å–ª–∏ –≤ —Å–µ—Å—Å–∏–∏ –µ—Å—Ç—å –≥–æ—Ç–æ–≤—ã–π –ø–ª–∞–Ω, –ø–æ–∫–∞–∑—ã–≤–∞–µ–º –µ–≥–æ
        if st.session_state.get("content_plan"):
            st.markdown("### üìä –í–∞—à –∫–æ–Ω—Ç–µ–Ω—Ç-–ø–ª–∞–Ω")
            
            # –ö–æ–Ω–≤–µ—Ä—Ç–∏—Ä—É–µ–º –¥–∞–Ω–Ω—ã–µ –≤ DataFrame –¥–ª—è –∫—Ä–∞—Å–∏–≤–æ–≥–æ –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è
            df = pd.DataFrame(st.session_state.content_plan)
            
            # –ü–µ—Ä–µ–∏–º–µ–Ω–æ–≤—ã–≤–∞–µ–º –∫–æ–ª–æ–Ω–∫–∏ –¥–ª—è –≤—ã–≤–æ–¥–∞ –Ω–∞ —Ä—É—Å—Å–∫–æ–º —è–∑—ã–∫–µ
            df.columns = ["–ù–µ–¥–µ–ª—è", "–î–µ–Ω—å –Ω–µ–¥–µ–ª–∏", "–¢–µ–º–∞ –ø–æ—Å—Ç–∞", "–ö–∞–Ω–∞–ª", "–§–æ—Ä–º–∞—Ç"]
            
            # –í—ã–≤–æ–¥–∏–º –∏–Ω—Ç–µ—Ä–∞–∫—Ç–∏–≤–Ω—É—é —Ç–∞–±–ª–∏—Ü—É –Ω–∞ –≤–µ—Å—å —ç–∫—Ä–∞–Ω
            st.dataframe(df, width='stretch', hide_index=True)

            st.markdown("---")
            col_dl1, col_dl2 = st.columns([1, 1])
            
            with col_dl1:
                # –≠–∫—Å–ø–æ—Ä—Ç –≤ CSV (–¥–æ–±–∞–≤–ª—è–µ–º -sig –¥–ª—è —Å–æ–≤–º–µ—Å—Ç–∏–º–æ—Å—Ç–∏ —Å Microsoft Excel –∏ —Ä—É—Å—Å–∫–∏–º–∏ –±—É–∫–≤–∞–º–∏)
                csv = df.to_csv(index=False).encode('utf-8-sig')
                st.download_button(
                    label="üì• –°–∫–∞—á–∞—Ç—å –∫–∞–∫ CSV (Excel)",
                    data=csv,
                    file_name="content_plan.csv",
                    mime="text/csv",
                    width='stretch'
                )
# –¢–æ—á–∫–∞ –≤—Ö–æ–¥–∞ –¥–ª—è —Ä–µ–Ω–¥–µ—Ä–∞ —Å—Ç—Ä–∞–Ω–∏—Ü—ã Streamlit
render_plan_tab()