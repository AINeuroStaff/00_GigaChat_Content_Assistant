            target_keywords = st.text_area("SEO –∫–ª—é—á–µ–≤—ã–µ —Å–ª–æ–≤–∞ (—á–µ—Ä–µ–∑ –∑–∞–ø—è—Ç—É—é):", placeholder="–∫—É–ø–∏—Ç—å —Å–ª–æ–Ω–∞, —Å–ª–æ–Ω—ã –º–æ—Å–∫–≤–∞, –¥–æ—Å—Ç–∞–≤–∫–∞ —Å–ª–æ–Ω–æ–≤")
            length = st.selectbox("–û–±—ä–µ–º —Å—Ç–∞—Ç—å–∏", ["–°—Ä–µ–¥–Ω—è—è (~3000-5000 —Å–∏–º–≤–æ–ª–æ–≤)", "–î–ª–∏–Ω–Ω–∞—è –ª–æ–Ω–≥—Ä–∏–¥ (~7000-10000 —Å–∏–º–≤–æ–ª–æ–≤)"])
            
