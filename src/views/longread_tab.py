def render_longread_tab() -> None:
    """–û—Ç—Ä–∏—Å–æ–≤—ã–≤–∞–µ—Ç –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å –≤–∫–ª–∞–¥–∫–∏ –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏ SEO-—Å—Ç–∞—Ç–µ–π –∏ –ª–æ–Ω–≥—Ä–∏–¥–æ–≤."""
    st.title("üìù –õ–æ–Ω–≥—Ä–∏–¥—ã –∏ SEO-—Å—Ç–∞—Ç—å–∏")
    st.markdown("–ì–µ–Ω–µ—Ä–∞—Ü–∏—è –æ–±—ä–µ–º–Ω—ã—Ö, —Å—Ç—Ä—É–∫—Ç—É—Ä–∏—Ä–æ–≤–∞–Ω–Ω—ã—Ö —Å—Ç–∞—Ç–µ–π –¥–ª—è –±–ª–æ–≥–æ–≤ (VC.ru, –î–∑–µ–Ω, —Å–∞–π—Ç) —Å —É—á–µ—Ç–æ–º –∫–ª—é—á–µ–≤—ã—Ö —Å–ª–æ–≤.")

    # –°–æ–∑–¥–∞–µ–º –¥–≤—É—Ö–∫–æ–ª–æ–Ω–æ—á–Ω—ã–π –º–∞–∫–µ—Ç: —Å–ª–µ–≤–∞ –Ω–∞—Å—Ç—Ä–æ–π–∫–∏ (1 —á–∞—Å—Ç—å), —Å–ø—Ä–∞–≤–∞ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã (2.5 —á–∞—Å—Ç–∏)
    col_settings, col_results = st.columns([1, 2.5])

    # ==========================================
    # --- 2. –ò–ù–¢–ï–†–§–ï–ô–° –ù–ê–°–¢–†–û–ï–ö ---
    # ==========================================
    
